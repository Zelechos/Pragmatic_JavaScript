'use strict'

// Objeto Math
console.log(Math);
console.log(Math.PI);
console.log(Math.abs(-3.42));

// redondea para arriba
console.log(Math.ceil(5.23));

// redondea para abajo
console.log(Math.floor(5.7894));

// redondea segun diga el decimal
console.log(Math.round(6.234));

// raiz cuadrada
console.log(Math.sqrt(9));

// para exponentes
console.log(Math.pow(2,5));

// indicador si el numero es negactivo o positivo ...
console.log(Math.sign(5));

// metodo randon
console.log(Math.random());

// numero aleatorio del 1 al 1000
console.log(Math.round(Math.random() * 1000));